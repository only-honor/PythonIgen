#print 1 to 10
print("Printing 1 to 10....")
i = 1
while i <= 10:
    print(i)
    i = i + 1

#print 0 - 20 Even
print("Printing 1 to 20 Even....")

num = 0
while num <= 20:
    if num%2==0:
        print(num)
    num = num + 1

#print 0 - 20 Odd
print("Printing 1 to 20 Odd....")
num1 = 0
while num1 <= 20:
    if num1%2!=0:
        print(num1)
    num1 = num1 + 1

#print 50 to 1 Reverse
print("Printing from 50 to 1 in reverse...")
num2 = 50
while num2 > 0:
    print(num2)
    num2 = num2 - 1

# Printing table of 7:
print("Printing Table of 7...")

num3 = 1
n = 7
