#swapcase: Converts lower to upper and upper to lower

str1 = "I am from india"
str2 = "I AM FROM INDIA"
print(str1.swapcase()) #"i AM FROM INDIA"
print(str2.swapcase())

#Partition()
str3 = "I am Arshdeep Singh and I am currently pursuing BTech"
print(str3.partition('am')) #separates the word while traversing on FCFS basis

#count()
str4 = "To be or not to be and let it be be be"
print(str4.count("be")) #Counting no. of be's

